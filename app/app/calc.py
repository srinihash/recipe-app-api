
def add(x, y):
    """
    Add two numbers x, y together and return back the result
    :param x:
    :param y:
    :return:
    """
    return x + y


def subtract(x, y):
    """
    Subtract two numbers x, y together and return back the result
    :param x:
    :param y:
    :return:
    """
    return y - x
